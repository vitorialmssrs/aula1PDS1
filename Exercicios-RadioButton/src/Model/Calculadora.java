package Model;

public class Calculadora {
	
	//classe que faz as contas para a janela calculadora
	
	public int adicao (int n1, int n2) {
		
		int resultadoSoma = n1 + n2;
		
		return resultadoSoma;
	}

	

	public int subtracao (int n3, int n4 ) {
		
		int resultadoSub = n3 - n4;
		
		return resultadoSub;
		
	}
	
	public int multiplicacao ( int n5, int n6) {
		
		int resultadoMult = n5 * n6;
		
		return resultadoMult;
		
	}
	
	public float divisao ( float n7, float n8) {
		
		float resultadoDivisao = n7 / n8;
		return resultadoDivisao;
		
	}
}
