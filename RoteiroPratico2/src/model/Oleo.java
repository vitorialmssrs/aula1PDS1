package model;

public class Oleo {

	public float frascoML (float valor, float quant) {
		float valorTot = valor*quant;
		return valorTot;
	}
	
	public float frascoL (float valor, float quant) {
		float valorTot = valor*quant;
		return valorTot;
	}
	
	public float total (float v, float v2) {
		float resp = v+v2;
		return resp;
	}
}
